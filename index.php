    $tutorNames = $tutorImages = array();
    while ($row = $result->fetch_assoc()) {
        $imgSrc = "";
        if (!isset( $tutorNames[$row["id"]]) ) {
            $tutorNames[$row["id"]] = $row["name"];
            if (isset($row["filename"])) {
                $imgSrc = "images/" . $row["filename"];
            } else {
                $imgSrc = "notfound.png";
            }
            $tutorImages[$row["id"]] = $imgSrc;
        }
    }
    $i = 0;
    foreach ($tutorNames as $tutID => $tutName) {
        $top = (intdivs($i++, 2)+1) * 15 + 50;
