$sql = "SELECT fpts_tutor_names.id, fpts_tutor_names.name,
        fpts_tutor_images.filename FROM
        fpts_tutor_names LEFT JOIN fpts_tutor_images
        ON fpts_tutor_names.id = fpts_tutor_images.tutor
        ORDER BY fpts_tutor_names.name, fpts_tutor_images.filename DESC";
$result = $cxn->query($sql);
$prevTutors = array();
$s = "                    ";

