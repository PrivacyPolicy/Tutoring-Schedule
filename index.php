        echo "            <div>" . $tutName;
        echo "</div>\n$s        </div>\n$s    </a>\n$s</div>\n";
    }
    echo "$s<div id=dumbySpacer style=\"top: ";
    $i = ($i < 12) ? 12 : $i;
    echo (intdivs(++$i, 2)+1) * 15 + 50;
    echo "%\"></div>\n";
} else {
    echo "No tutors or error.<br>\n";
